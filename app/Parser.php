use Exception;

final class Parser
{
    // Memory usage is expected to be high, but limited by the chunk size, so limit it to what is
    // likely available on the 12GB benchmark machine.
    const MEMORY_LIMIT = '8192M';

    // Most URLs are less than 100 characters, so 256 is a safe buffer size to find at least one
    // newline.
    const NEWLINE_SEARCH_SIZE = 256;

    // 16MB read/write buffers.
    const STREAM_BUFFER_SIZE = 1 << 20;

    // 128MB chunk size when reading input.
    const READ_CHUNK_SIZE = 8 << 20;

    // Number of workers to split the input into.
    const WORKER_COUNT = 8;

    // A line is ±75 characters, so 75 * 2^14 = 1.2 MiB, which is a good buffer size for this input,
    // so set a mask to quickly check when this many lines have been buffered.
    const LINE_BUFFER_MASK = (1 << 14) - 1;

    // Visit.php contains just under 300 URIs, so let us assume it won't be much more than that.
    const URI_LIMIT = 300;

    // Strip this prefix from every URI (length = 19).
    const URI_PREFIX_LEN = 19;

    // The timestamp is always the same length.
    const TIMESTAMP_LEN = 26;

    // As is the date
    const DATE_LEN = 10;

    private float $startTime;
    private ?float $previousTime = null;

    public function parse(string $inputPath, string $outputPath): void
    {
        // Get the starting time for logging elapsed time for each step.
        $this->startTime = \microtime(true);

        // Don't bother with garbage collection, to avoid any random slowdowns.
        \gc_disable();

        \ini_set('memory_limit', self::MEMORY_LIMIT);

        $fileSize = \filesize($inputPath);
        $splitSize = (int)($fileSize / self::WORKER_COUNT);
        // The first worker will always start at the beginning of the file, so start with that
        // split point.
        $splits = [0];

        // Calculate split points for the remaining workers by finding the first newline after
        // the split point, and positioning the split after it. This ensures that workers are
        // always processing whole lines.
        $input = \fopen($inputPath, 'rb');
        \stream_set_read_buffer($input,  self::NEWLINE_SEARCH_SIZE);
        for ($i = 1; $i < self::WORKER_COUNT; $i++) {
            \fseek($input, $i * $splitSize);
            $data = \fread($input, self::NEWLINE_SEARCH_SIZE);

            // Find the first newline in the after the split point.
            $nl = \strpos($data, "\n");

            // Bail if the newline is not found, so an error is thrown instead of silently producing
            // incorrect output.
            if ($nl === false) {
                throw new Exception("Failed to find newline after split point for worker $i");
            }

            $splits[] = \ftell($input) - \strlen($data) + $nl + 1;
        }
        $splits[] = $fileSize;

        $this->logTime("Split");

        for ($i = 0; $i < self::WORKER_COUNT; $i++) {
            \socket_create_pair(AF_UNIX, SOCK_STREAM, 0, $sockets[$i]);
            $pid = \pcntl_fork();
            if ($pid === 0) {
                // Each worker closes the parent end of the socket,...
                \socket_close($sockets[$i][0]);

                // ...sends the results back to the parent through the socket, and exits.
                \socket_write($sockets[$i][1], self::serialize($this->parseSection($inputPath, $splits[$i], $splits[$i + 1])));
                \socket_close($sockets[$i][1]);
                exit(0);
            }
            socket_close($sockets[$i][1]);
        }

        // Fetch and unserialize the results from the workers.
        foreach ($sockets as $pair) {
            $data = '';
            while ($chunk = \socket_read($pair[0], self::STREAM_BUFFER_SIZE)) $data .= $chunk;
            $results[] = self::unserialize($data);
            \socket_close($pair[0]);
        }

        $this->logTime('Parsed');

        // Take the first worker results as the starting point, and merge the results of the other
        // workers into it.
        [$uris, $counters] = $results[0];
        $dateCount = count($this->dates);
        for($i = 1; $i < self::WORKER_COUNT; $i++) {
            [$workerUris, $workerCounters] = $results[$i];
            foreach($workerUris as $workerUri => $workerUriIndex) {
                $uriIndex = $uris[$workerUri] ?? null;
                if ($uriIndex === null) {
                    $uriIndex = $uris[$workerUri] = count($uris);
                }
                foreach($this->dates as $date => $dateIndex) {
                    $count = $workerCounters[$workerUriIndex * $dateCount + $dateIndex];
                    if ($count > 0) {
                        $counterIndex = $uriIndex * $dateCount + $dateIndex;
                        $counters[$counterIndex] += $count;
                    }
                }
            }
        }

        $this->logTime('Merged');

        $output = \fopen($outputPath, 'wb');
        \stream_set_write_buffer($output, self::STREAM_BUFFER_SIZE);
        $buffer = "{";
        $lines = 0;
        $firstUri = true;
        // Output validation requires that the URIs are the order they were first encountered in the
        // input.
        foreach ($uris as $uri => $uriIndex) {
            if (!$firstUri) {
                $buffer .= ",";
            }
            $firstUri = false;

            // Output validation requires that the URI slashes are escaped.
            $buffer .= "\n    \"" . \str_replace('/', '\\/', $uri) . "\": {";

            $firstDate = true;

            $uriOffset = $uriIndex * $dateCount;
            foreach ($this->dates as $date => $dateIndex) {
                if (!$count = $counters[$uriOffset + $dateIndex]) {
                    continue;
                }

                if (!$firstDate) {
                    $buffer .=  ",";
                }
                $firstDate = false;
                $buffer .=  "\n        \"$date\": $count";
                if ((++$lines & self::LINE_BUFFER_MASK) === 0) {
                    \fwrite($output, $buffer);
                    $buffer = '';
                }
            }
            $buffer .= "\n    }";
        }
        $buffer .= "\n}";
        \fwrite($output, $buffer);
        @\fclose($output);

        $this->logTime('Written');
    }

    private function parseSection($inputPath, $start, $end): array
    {
        $input = \fopen($inputPath, 'rb');
        \stream_set_read_buffer($input,  self::STREAM_BUFFER_SIZE);

        $dateCount = count($this->dates);

        $counters = \array_fill(0, self::URI_LIMIT * count($this->dates), 0);
        $uris = [];
        $uriSequence = 0;

        // Move the file pointer to the start of the section.
        \fseek($input, $start);

        $chunk = '';
        $chunkStart = 0;
        $current = $start;
        while ($current < $end) {
            $chunkSize = \min(self::READ_CHUNK_SIZE, $end - $current);
            $chunk = \substr($chunk, $chunkStart) . \fread($input, $chunkSize);
            $chunkStart = 0;
            $current += $chunkSize;

            while (true) {
                // Try and find a line ending, and we've run out, go onto the next chunk.
                if (($lineEnd = \strpos($chunk, "\n", $chunkStart)) === false) {
                    break;
                }

                // Find the comma position based on the line end.
                $comma = $lineEnd - self::TIMESTAMP_LEN;

                $uriStart = $chunkStart + self::URI_PREFIX_LEN;
                $uri  = \substr($chunk, $uriStart, $comma - $uriStart);
                if (($uriIndex = $uris[$uri] ?? null) === null) {
                    $uriIndex = $uris[$uri] = $uriSequence++;
                }

                $dateIndex = $this->dates[\substr($chunk, $comma + 1, self::DATE_LEN)];

                $counters[$uriIndex * $dateCount + $dateIndex]++;

                $chunkStart = $lineEnd + 1;
            }
        }

        \fclose($input);

        return [$uris, $counters];
    }

    /**
     * Use the start time to calculate the elapsed time and output a message with the elapsed,
     * formatted time in seconds.
     */
    private function logTime(string $message): void
    {
        $elapsedTotal = number_format(\microtime(true) - $this->startTime, 4);
        $elapsedPrevious = number_format(\microtime(true) - ($this->previousTime ?? $this->startTime), 4);

        $this->previousTime = \microtime(true);

        echo "$message in $elapsedPrevious seconds, $elapsedTotal seconds in total.\n";
    }

    private static function serialize(mixed $data): string
    {
        if (\function_exists('igbinary_serialize')) {
            return \igbinary_serialize($data);
        }
        return \serialize($data);
    }

    private static function unserialize(string $data): mixed
    {
        if (\function_exists('igbinary_unserialize')) {
            return \igbinary_unserialize($data);
        }
        return \unserialize($data);
    }

